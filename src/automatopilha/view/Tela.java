    private void btnAdicionarTransicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarTransicaoActionPerformed
        // TODO add your handling code here:
        boolean ehValido = true;
        if (comboEstadoAtualTransicao.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Deve ser informado um estado atual para transi√ß√£o");
            ehValido = false;
        }
        if (comboValorPilhaTransicao.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Deve ser informado um valor atual da pilha para transi√ß√£o");
            ehValido = false;
        }        
        if (comboProximoEstadoTransicao.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Deve ser informado um pr√≥ximo estado para transi√ß√£o");
            ehValido = false;
        }
        
        if (ehValido) {
            Estado estadoAtualTransicao = automato.buscarEstadoPorNome((String)comboEstadoAtualTransicao.getSelectedItem());
            Estado proximoEstadoTransicao = automato.buscarEstadoPorNome((String)comboProximoEstadoTransicao.getSelectedItem());
            String valorEntradaTransicao = null;
            if (comboEntradaTransicao.getSelectedIndex() > 0) {
                valorEntradaTransicao = comboEntradaTransicao.getSelectedItem().toString();
            }
            String valorAtualPilhaTransicao = comboValorPilhaTransicao.getSelectedItem().toString();
            String valorEntradaPilhaTransicao = null;
            if (comboValorEntradaPilhaTransicao.getSelectedIndex() > 0){
                valorEntradaPilhaTransicao = comboValorEntradaPilhaTransicao.getSelectedItem().toString();
            }
            
            Transicao transicao = new Transicao();
            transicao.setEntrada(valorEntradaTransicao);
            transicao.setEstadoAtual(estadoAtualTransicao);
            transicao.setProximoEstado(proximoEstadoTransicao);
            transicao.setValorEntradaPilha(valorEntradaPilhaTransicao);
            transicao.setValorPilha(valorAtualPilhaTransicao);
            
            modelTransicoes.addRow(transicao);
            
            comboEstadoAtualTransicao.setSelectedIndex(0);
            comboEntradaTransicao.setSelectedIndex(0);
            comboValorPilhaTransicao.setSelectedIndex(0);
            comboValorEntradaPilhaTransicao.setSelectedIndex(0);
            comboProximoEstadoTransicao.setSelectedIndex(0);
        }
        
    }//GEN-LAST:event_btnAdicionarTransicaoActionPerformed

